    [GetInvoice]    VARCHAR (1)   DEFAULT (' ') NOT NULL,
    [GetShipNotice] VARCHAR (1)   DEFAULT ('N') NOT NULL,
    CONSTRAINT [People_INDEX01] PRIMARY KEY CLUSTERED ([ID] ASC)
