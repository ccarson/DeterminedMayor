    [Recnum]        BIGINT        IDENTITY (1, 1) NOT NULL,
    [ID]            DECIMAL (10)  DEFAULT ((0)) NOT NULL,
    [CUSTID]        VARCHAR (10)  DEFAULT (' ') NOT NULL,
    [PREFIX]        VARCHAR (4)   DEFAULT (' ') NOT NULL,
    [FIRST_NAME]    VARCHAR (20)  DEFAULT (' ') NOT NULL,
    [INITIAL]       VARCHAR (1)   DEFAULT (' ') NOT NULL,
    [LAST_NAME]     VARCHAR (30)  DEFAULT (' ') NOT NULL,
    [SUFFIX]        VARCHAR (4)   DEFAULT (' ') NOT NULL,
    [EMAIL]         VARCHAR (100) DEFAULT (' ') NOT NULL,
    [TITLE]         VARCHAR (20)  DEFAULT (' ') NOT NULL,
    [EXT]           VARCHAR (4)   DEFAULT (' ') NOT NULL,
